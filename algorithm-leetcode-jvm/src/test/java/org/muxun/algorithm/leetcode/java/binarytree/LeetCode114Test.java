@DisplayName("二叉树展开为链表测试用例")
public class LeetCode114Test {

    @Test
    public void testOne() {
        TreeNode root = new TreeNode(1);
        root.left = new TreeNode(2);
        root.right = new TreeNode(5);
        root.left.left = new TreeNode(3);
        root.left.right = new TreeNode(4);
        root.right.right = new TreeNode(6);
        TreeNode expected = new TreeNode(1);
        expected.right = new TreeNode(2);
        expected.right.right = new TreeNode(3);
        expected.right.right.right = new TreeNode(4);
        expected.right.right.right.right = new TreeNode(5);
        expected.right.right.right.right.right = new TreeNode(6);
        new LeetCode114().flatten(root);
        while (expected != null) {
            Assertions.assertEquals(expected.val, root.val);
            expected = expected.right;
            root = root.right;
        }
    }
}
